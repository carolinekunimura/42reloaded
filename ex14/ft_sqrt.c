int	ft_sqrt(int nb)
{
	int	r;
	int	i;

	r = 0;
	i = 1;
	if (nb <= 0)
	{
		return (0);
	}	
	while (r < nb)
	{
		r = (i * i);
		if (r == nb)
		{
			return (i);
		}
		if (i > 46340)
		{
			return (0);
		}
	i++;
	}
	return (0);
}
